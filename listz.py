#!/usr/bin/python3.5

# list : mutable sequences of bytes
num_lst = [1, 2, 5]
str_lst = ['apple', 'orange', 'cherry']
mixed_lst = [1, 'apple', None]

# print entire list
print("num_lst = ", num_lst)
print("str_lst = ", str_lst)
print("mixed_lst = ", mixed_lst)

# print list element
print("num_lst[1] = ", num_lst[1])
print("str_lst[1] = ", str_lst[1])
print("mixed_lst[1] = ", mixed_lst[1])
